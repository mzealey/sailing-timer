export const getYardstick = (competitor) => {
  if (!competitor || !competitor.boatClass) return null;

  const classData = boatClasses.find((c) => c.title === competitor.boatClass);

  if (!classData) return null;

  if (competitor.crew && classData.yardstickWithCrew)
    return classData.yardstickWithCrew;

  return classData.yardstick;
};
